package lesson10_static;

public class Calculator {
    public static final double PI = 3.14;

    public static int sum(int a, int b) {
        return a + b;
    }

    public static int minus(int a, int b) {
        return a - b;
    }

    public static double circleSquare(double r) {
        return PI * r * r;
    }


}
